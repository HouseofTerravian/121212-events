export function getEventBySlug(slug: string): Event | undefined {
  return events.find((event) => event.slug === slug)
}

export function getFeaturedEvents(): Event[] {
  return events.filter((event) => event.featured)
}

export function getEventsByCity(citySlug: string): Event[] {
  return events.filter((event) => event.citySlug === citySlug)
}

export function getEventsByCategory(catSlug: string): Event[] {
  return events.filter((event) => event.catSlug === catSlug)
}

export function getRelatedEvents(event: Event, limit: number = 3): Event[] {
  return events
    .filter(
      (e) =>
        e.id !== event.id &&
        (e.citySlug === event.citySlug || e.catSlug === event.catSlug)
    )
    .slice(0, limit)
}

export function getSortedEvents(): Event[] {
  return [...events].sort((a, b) => a.dateSort.localeCompare(b.dateSort))
}
